    private void CargarComboBox() {
        
        for (Alumno alumno : alumnos) {
         
              JCBverAlumno.addItem(alumno);
            
        }
                
        for (Materia materia : materias) {
         
              JCBverMateria.addItem(materia);
            
        }
        
    }
